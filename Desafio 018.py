import math
n = float(input('Digite o valor do 창ngulo: '))

print(f'O valor do 창ngulo convertido p/ seno: {math.sin(math.radians(n)):.3f}')
print(f'O valor do 창ngulo convertido p/ cosseno: {math.cos(math.radians(n)):.3f} ')
print(f'O valor do 창ngulo convertido p/ tangente: {math.tan(math.radians(n)):.3f} ')
